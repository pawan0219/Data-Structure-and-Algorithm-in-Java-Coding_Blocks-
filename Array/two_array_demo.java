package Array;

public class two_array_demo {
    public static void main(String[] args) {
          int[][] arr = new int[3][4];
          System.out.println(arr[1]);
          System.out.println(arr[2]);
          int ar[][] = new int[3][];
          System.out.println(ar);
          int [][] other = arr;
          System.out.println(other);
    }
}
